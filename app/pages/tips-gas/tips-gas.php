			<h1>Einfach sparen mit 10 Gas-Spartipp's</h1>
			<p> Da die Energiepreise seit Jahren steigen, möchten viele Verbraucher wissen, wie sie Gas sparen können. Zum Glück gibt es zahlreiche Möglichkeiten, wie sich der Gasverbrauch reduzieren lässt. Häufig reichen dafür einfache Maßnahmen aus. Da ein Großteil des Verbrauchs auf die Heizwärme und die Warmwasseraufbereitung zurückgeht, lohnt es sich, an diesen Stellen anzusetzen. Auf den gewohnten Komfort braucht dafür niemand zu verzichten. Ein positiver Nebeneffekt: Als Verbraucher sparen Sie beim Gas und schonen dabei die Umwelt.</p>
		</section>
		<section>
			<h3>Tipp 1: Die optimale Raumtemperatur einstellen</h3>
			<p>Wohnzimmer, Schlafzimmer, Küche oder Bad – jedes Zimmer hat eine eigene ideale Raumtemperatur. Beispielsweise fühlen sich die meisten Menschen im Badezimmer bei einer anderen Temperatur wohl als im Wohnzimmer. Gas können Sie sparen, indem Sie die Räume nicht überheizen, sondern berücksichtigen, wofür und wie häufig die Zimmer genutzt werden. Hinsichtlich der optimalen Wärme dienen die folgenden Werte als Orientierung:</p>
				<li>Wohnzimmer: 20 bis 22 °C</li>
				<li>Badezimmer: 22 bis 24 °C</li>
				<li>Küche: 16 bis 18 °C</li>
				<li>Schlafzimmer: 15 bis 18 °C</li>
				<li>Flur: 15 bis 16 °C</li>
			<p>Schon eine Senkung der Raumtemperatur um ein Grad Celsius senkt die Heizkosten um etwa sechs Prozent. Mit dem Thermostat-Ventil am Heizkörper lässt sich die gewünschte Temperatur auf das Grad genau einstellen.
			</p>
		</section>
		<section>
			<h3>Tipp 2: Räume nicht auskühlen lassen</h3>
			<p>Wer seine Wohnung kontinuierlich heizt, kann viel Gas sparen. Die Wände der Wohnung geben nämlich konstant Wärme ab. Daraus ergibt sich, dass zum Erwärmen eines ausgekühlten Raumes überdurchschnittlich viel Energie verbraucht wird. Mit einem programmierbaren Thermostat lässt sich die Zimmertemperatur in den Zeiträumen verringern, in denen Sie nicht zu Hause sind.</p>
		</section>
		<section>
			<h3>Tipp 3: Kurz und gründlich lüften</h3>
			<p>Auch durch richtiges Lüften können Verbraucher Heizkosten sparen. In beheizten Zimmern sollten die Fenster nicht auf Kipp stehen, da durch den Spalt kontinuierlich Wärme nach draußen entweicht. Besser ist mehrmaliges Stoßlüften für etwa fünf bis zehn Minuten. Wenn beim Lüften die Heizung abgeschaltet wird, lässt sich zusätzlich Gas sparen.</p>	
		</section>
		<section>
			<h3>Tipp 4: Regelmäßige Wartung der Heizung</h3>
			<p>Auch die jährliche Wartung der Heizungsanlage durch eine Fachkraft trägt dazu bei, den Gasverbrauch zu senken. Im Zuge der Instandhaltung prüft der Installateur die Anlage auf Fehlfunktionen und defekte Bauteile. Insbesondere den Gasbrenner der Heizung wird er unter die Lupe nehmen. Schließlich treten deutliche Effizienzverluste auf, wenn das Gas nicht vollständig verbrennt.</p>
		</section>
		<section>
			<h3>Tipp 5: Heizung entlüften</h3>
			<p>Gibt eine Heizung gluckernde Geräusche von sich, befindet sich höchstwahrscheinlich Luft im Heizkörper. Infolgedessen verteilt sich das aufgeheizte Wasser nicht optimal, wodurch die Wärmeleistung sinkt. Die Heizung können Sie unkompliziert selbst entlüften, indem Sie mit einem Schraubenzieher oder Spezialschlüssel das Heizungsventil öffnen.</p>	
		</section>
		<section>
			<h3>Tipp 6: Heizkörper nicht zustellen</h3>
			<p>Um Gas zu sparen, ist eine gute Luftzirkulation erforderlich, denn nur dann kann sich die Wärme im Zimmer verteilen. Stehen Gegenstände vor den Heizkörpern, staut sich die Wärme dahinter. Falls sich das Thermostat direkt an der Heizung befindet, fährt es die Heizleistung herunter – schließlich misst es eine ausreichende Raumtemperatur. Die Folge: Das Zimmer bleibt kalt beziehungsweise kühlt sich ab. Abhilfe schafft ein an anderer Stelle im Raum angebrachter Heizfühler, mit dem sich die tatsächliche Zimmertemperatur messen lässt.</p>
		</section>
		<section>
			<h3>Tipp 7: Wasser sparen</h3>
			<p>Es ist kosteneffizienter, Wasser mit Gas statt mit Strom zu erwärmen. Um die Heizkosten zu senken, sollten Sie eine Dusche einem Vollbad vorziehen. Wer auf ein Bad verzichtet, kann dafür durchschnittlich dreimal duschen. Noch weniger Warmwasser verbrauchen Sie mit einem Sparduschkopf.<br></br>
			Darüber hinaus empfiehlt es sich, Geräte wie die Waschmaschine oder den Geschirrspüler an die Warmwasserversorgung der Heizungsanlage anzuschließen. Mit dieser Maßnahme lässt sich der Energieverbrauch der angeschlossenen Geräte um bis zu 20 Prozent senken.</p>
		</section>
		<section>
			<h3>Tipp 8: Türen geschlossen halten</h3>
			<p>Vor allem in der Heizperiode sollten die Zimmertüren stets geschlossen sein, damit keine Wärme entweicht. Das ist umso wichtiger, je größer die Temperaturunterschiede zwischen den Räumen sind.</p>
		</section>
		<section>
			<h3>Tipp 9: Türen und Fenster abdichten</h3>
			<p>Sind Fenster und/oder Türen undicht, entweicht Raumwärme durch die kleinen Spalten. Diese lassen sich meistens mit Isolierklebeband oder Dichtungsbändern abdichten; auch ein Zugluftstopper schafft Abhilfe. Sind an den Fenstern Rollläden angebracht, sollten diese nachts geschlossen werden. Das senkt den durch die Fenster bedingten Wärmeverlust um etwa 30 Prozent, sodass Verbraucher mit dieser einfachen Maßnahme Gas sparen.</p>
		</section>
		<section>
			<h3>Tipp 10: Gas sparen beim Kochen und Backen</h3>
			<p>Fürs Kochen gibt es gleich mehrere Tipps, mit denen Sie den Gasverbrauch senken können. Gas sparen Sie, wenn Sie die folgenden Ratschläge beherzigen:</p>
			<li>beim Kochen stets den Deckel auf den Topf legen</li>
			<li>die zur Topfgröße passende Herdplatte nutzen</li>
			<li>möglichst wenig Flüssigkeit zum Garen verwenden</li>
			<li>beim Backen in einem Gasherd Umluft statt Ober- und Unterhitze nutzen</li>
